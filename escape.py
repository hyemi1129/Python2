# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# \" \' : 문장 내 따옴표
# 저는 "이름"입니다.
print('저는 "이름"입니다.')
print("저는 \"이름\"입니다.")

# \\ : 문장 내 \
print("C:\\Users\\gimhy\\OneDrive\\바탕 화면\\PythonWorkspace>")

# \r : 커서 맨 앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")